class Student
{
private String Sname;
private int Srno;
private int Smarks;
public void setSname(String Sname)
{
this.Sname=Sname;
}
public void setSrno(int Srno)
{
this.Srno=Srno;
}
public void setSmarks(int Smarks)
{
this.Smarks=Smarks;
}
public String getSname()
{
return Sname;
}
public int getSmarks()
{
return Smarks;
}
public int getSrno()
{
return Srno;
}
}